// Get localized language name
export function getLocalizedLanguageName(langCode: string, uiLocale: string = "en"): string {
  return LANGUAGE_NAMES[uiLocale]?.[langCode] || LANGUAGE_NAMES.en[langCode] || langCode;
}

// Get language alphabet
export function getLanguageAlphabet(langCode: string): string {
  return LANGUAGE_ALPHABETS[langCode] || "latin";
}

// Create language object with localized name
export function createLanguage(langCode: string, uiLocale: string = "en"): Language {
  return {
    value: langCode,
    label: getLocalizedLanguageName(langCode, uiLocale),
    alphabet: getLanguageAlphabet(langCode),
    nativeName: LANGUAGE_NAMES[langCode]?.[langCode] || getLocalizedLanguageName(langCode, uiLocale)
  };
}

// Get all languages with localized names
export function getAllLanguages(uiLocale: string = "en"): Language[] {
  return Object.keys(LANGUAGE_NAMES.en).map(langCode => createLanguage(langCode, uiLocale));
}

